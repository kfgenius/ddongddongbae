void CSpr::SetSpr(int vx, int vy, int vtype, int h)
{
	//초기화
	life=true;
	ani_tempo=tempo=0;
	invincible=0;
	dir=0;

	x=vx;
	y=vy;
	hp=h;
	type=vtype;

	//종류에 맞는 파워와 스피드등을 부여
	int spd[]={1,1,0,1,0,1,4};
	int xs[]={24,24,24,24,24,24,24};
	int ys[]={32,32,32,32,32,32,32};
	int dirt[]={0,1,1,1,1,1,1};

	power=0;
	speed=spd[type];
	x_size=xs[type];
	y_size=ys[type];
	dir_type=dirt[type];
}

void CSpr::Move()
{
	if(type==1)	//일드록
	{
		if(dir==0)y-=speed;
			else y+=speed;
		if(y>200)dir=0;
			else if(y<10)dir=1;
	}
	else if(type==3)	//미라
	{
		if(tempo<300){ x+=rand()%5-3; y+=rand()%5-2; }
			else if(tempo<600){ x+=rand()%5+1; y+=rand()%5-2; }
			else tempo=0;
	}
	else if(type==5)	//이소미
	{
		if(tempo==300)
		{
			x=rand()%(320-XSIZE);
			y=rand()%(Y_MAX-YSIZE);
			tempo=0;
		}
	}
	else if(type==6)	//유수염
	{
		if(dir==0){x=Min(320-x_size,x+speed);y=Max(0,y-speed);}
			else if(dir==1){x=Min(320-x_size,x+speed);y=Min(Y_MAX-y_size,y+speed);}
			else if(dir==2){x=Max(0,x-speed);y=Min(Y_MAX-y_size,y+speed);}
			else if(dir==3){x=Max(0,x-speed);y=Max(0,y-speed);}
		if(tempo==50)
		{
			dir=rand()%4;
			tempo=0;
		}
	}

	if((++tempo%10)==0)
	{
		if(++ani_tempo >= 4)ani_tempo=0;
	}

	x=Max(0,Min(320-x_size,x));
	y=Max(0,Min(Y_MAX-y_size,y));
}

void CSpr::Control()
{
	if(_GetKeyState(VK_UP)){ y=Max(0,y-speed); dir=0; }
		else if(_GetKeyState(VK_DOWN)){ y=Min(Y_MAX-y_size,y+speed); dir=2; }
	if(_GetKeyState(VK_LEFT)){ x=Max(0,x-speed); dir=3; }
		else if(_GetKeyState(VK_RIGHT)){ x=Min(320-x_size,x+speed); dir=1; }

	Move();
}

