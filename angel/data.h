int GetGradient(int x1, int y1, int x2, int y2)
{
	int dx=x2-x1;
	int dy=y2-y1;
	//4방향
	if(dx==0)
	{
		if(dy<=0)return 0;
			else return 8;
	}
	else if(dy==0)
	{
		if(dx>=0)return 4;
			else return 12;
	}
	//기타 방향
	int dg=(abs(dx)*10)/abs(dy);

	if(dx>=0 && dy<0)
	{
		if(dg<5)return 1;
			else if(dg<20)return 2;
			else if(dg<50)return 3;
			else return 4;
	}
	else if(dx>=0 && dy>=0)
	{
		if(dg<5)return 7;
			else if(dg<20)return 6;
			else if(dg<50)return 5;
			else return 4;
	}
	else if(dx<0 && dy>=0)
	{
		if(dg<5)return 9;
			else if(dg<20)return 10;
			else if(dg<50)return 11;
			else return 12;
	}
	else
	{
		if(dg<5)return 15;
			else if(dg<20)return 14;
			else if(dg<50)return 13;
			else return 12;
	}
}

