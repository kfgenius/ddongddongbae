#define SNR_START 5
//대화 출력
int CDlg::TextPrint(int n_dlg, int y)
{
	int length=strlen(snr[n_dlg]);
	char dlg_buffer[1024];
	strcpy(dlg_buffer,snr[n_dlg]);
	bool ani_end=false;

	//얼굴 알아내기
	char* name[]={"이동헌","일드록","김미노","미라","므족소녀","이소미","유수염","???"};
	char tmp[4];
	for(int i=0; i<2; i++)
		tmp[i]=dlg_buffer[i];
	tmp[2]=NULL;
	int name_no=atoi(tmp);
	if(name_no<16)m_sv.var[26]=name_no;

	//대화를 출력하기 알맞게 처리
	int sp=SNR_START, msp=SNR_START, space;	//처리되고 있는 포인트, 이전까지 처리한 포인트, space가 있던 곳
	while(sp<length)
	{
		if(dlg_buffer[sp]==' ')space=sp;
		if(dlg_buffer[sp]=='\n')msp=sp;
		if(sp-msp>=30)
		{
			dlg_buffer[space]='\n';
			msp=space;
		}
		sp++;
	}

	RECT text, shadow;
	SetRect(&text,0,y,320,240);
	SetRect(&shadow,1,y+1,320,240);

	sp=SNR_START;
	int n_of_e=0, tp=0;	//줄바꿈 횟수, 출력하는 단어
	while(1)
	{
		if(PeekMessage(&msg,NULL,0,0,PM_NOREMOVE))
		{
			if(!GetMessage(&msg,NULL,0,0)) break;
			TranslateMessage(&msg);
			DispatchMessage(&msg);
		}

		//대화출력
		char text_buffer[256];
		if(!ani_end)
		{
			if(sp<length && n_of_e<4)
			{
				if(dlg_buffer[sp]=='\n')n_of_e++;
				text_buffer[tp]=dlg_buffer[sp];
				if(dlg_buffer[sp]&0x80)	//한글일 경우 2칸 전진
				{
					sp++; tp++;
					text_buffer[tp]=dlg_buffer[sp];
				}
				text_buffer[tp+1]=NULL;
				//포인터 증가
				sp++; tp++;
			}
			else ani_end=true;
		}
		else
		{
			//다음으로
			if(_GetKeyState(VK_RETURN))
			{
				if(!enter)
				{
					enter=true;
					m_game.PlusTime(5);
					if(sp<length)
					{
						ani_end=false;
						n_of_e=0;
						tp=0;
					}
					else break;
				}
			}
			else enter=false;
		}
		//출력
		Background();
		jdd->DrawText(backbuffer,name[name_no%8],font20,1,141,JColor(0,0,0));
		jdd->DrawText(backbuffer,name[name_no%8],font20,0,140,JColor(0,216,0));
		jdd->DrawPicture(backbuffer,"대화창",0,160,NULL);
		jdd->DrawText(backbuffer,text_buffer,font20,&shadow,JColor(0,0,0));
		jdd->DrawText(backbuffer,text_buffer,font20,&text,JColor(255,255,255));
		m_game.Render();
		if(!ani_end && !_GetKeyState(VK_RETURN))Sleep(20);
	}

	//명령문 읽기
	for(i=0; i<3; i++)
		tmp[i]=dlg_buffer[i+2];
	tmp[3]=NULL;
	return atoi(tmp);
}

