int CBattle::MslNo(bool enemy)
{
	int start, id_max;
	if(enemy){ start=ENEMY_MISSILE; id_max=MISSILE_MAX; }
		else{ start=0; id_max=ENEMY_MISSILE; }
	for(int i=start; i<id_max; i++)
		if(!msl[i].life)return i;
	
	return -1;
}

int CBattle::SprNo()
{
	for(int i=0; i<SPRMAX; i++)
		if(spr[i].life)return i;
	
	return -1;
}

bool CBattle::Crush(int spr_no, int msl_no)
{
	int left1=spr[spr_no].x, left2=msl[msl_no].x;
	int top1=spr[spr_no].y, top2=msl[msl_no].y;
	int right1=spr[spr_no].x+spr[spr_no].x_size, right2=msl[msl_no].x+msl[msl_no].x_size;
	int bottom1=spr[spr_no].y+spr[spr_no].y_size, bottom2=msl[msl_no].y+msl[msl_no].y_size;
	
	if(left1<right2 && right1>left2 && top1<bottom2 && bottom1>top2)
		return true;
	else
		return false;
}

